package shopnoithat.Entity;

public class Menus {
	private int maDS;
	private String tenDS;
	private String url;
	public Menus() {
		super();
	}
	public int getMaDS() {
		return maDS;
	}
	public void setMaDS(int maDS) {
		this.maDS = maDS;
	}
	public String getTenDS() {
		return tenDS;
	}
	public void setTenDS(String tenDS) {
		this.tenDS = tenDS;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
