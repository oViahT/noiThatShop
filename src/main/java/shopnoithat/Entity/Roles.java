package shopnoithat.Entity;

public class Roles {
	private boolean id;
	private String name;

	public Roles() {
		super();
	}

	public boolean isId() {
		return id;
	}

	public void setId(boolean id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
